package com.cg.Exception;

public class RoomDetailException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public RoomDetailException() {
		super();
		
	}

	public RoomDetailException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	
	}

	public RoomDetailException(String message, Throwable cause) {
		super(message, cause);
	}

	public RoomDetailException(String message) {
		super(message);
	
	}

	public RoomDetailException(Throwable cause) {
		super(cause);
		
	}

	
	
}
